const incris = document.getElementsByClassName("increase");
const reset = document.getElementsByClassName("reset");
const decre = document.getElementsByClassName("decrease");
const count = document.querySelector(".count");

count = 0;
//increase
incris.addEventListener("click" , function(){
    for(let i=0 ; i++;){
        count =i;
        return count;
   }
});

//decrease

decre.addEventListener("click" , function(){
    for(let i=0 ; i--;){
        count =i;
        return count;
   }
});

reset.addEventListener("click" , function(){
    return reset = 0;

});
